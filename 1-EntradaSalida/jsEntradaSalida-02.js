/*
Debemos lograr tomar un nombre con 'prompt' 
y luego mostrarlo por 'alert' al presionar el bot√≥n  'mostrar'*/
function mostrar()
{
	
	let nombre; // "let" es reemplazo de "var". Se utiliza para variables

	nombre = prompt ("Ingrese su nombre"); // "prompt" es una ventana desplegable que deja introducir informacion, combinado con "alert" da una respuesta


	alert(nombre); // "alert" es una ventana desplegable que solo arroja una informacion
}

